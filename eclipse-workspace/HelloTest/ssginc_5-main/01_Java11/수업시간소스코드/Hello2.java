
public class Hello2 {

	public static void main(String[] args) {

		System.out.println("Hello World"); // String 타입, 참조형
		System.out.println("c:\\aab");     // String 타입, 참조형
		System.out.println("인경열");        // String 타입, 참조형
		
		System.out.println(35+2);  // 수치형(정수 타입)
		System.out.println('남');  // char 타입, 기본형
		System.out.println(183.56); // 수치형(실수 타입)
		System.out.println(67.34);  // 수치형(실수 타입)
		System.out.println(true); // false		
	}
}
