package exam01_데이터종류;

public class PDT02_실수형 {

	public static void main(String[] args) {
		
		// 실수형 리터럴 표현 방법
		
		// 1. float 표현식
		System.out.println(3.14F);
		System.out.println(3.14f);
		System.out.println(3F);
		
		// 2. double 표현식
		System.out.println(3.14); // 기본
		System.out.println(3.14d);
		System.out.println(3.14D);
		
		
		




	}

}
