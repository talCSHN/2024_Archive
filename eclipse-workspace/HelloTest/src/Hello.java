
public class Hello {

	public static void main(String[] args) {
		System.out.println("Hello World"); // String 타입, 참조형
		System.out.println("박관호");
		System.out.println(30); // 수치형, 정수 타입
		System.out.println('남'); // char 타입, 기본형
		System.out.println(175.0); // 수치형, 실수 타입
		System.out.println(82);
		System.out.println(false);

	}

}
